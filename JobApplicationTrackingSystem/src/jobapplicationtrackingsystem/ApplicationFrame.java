    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        // TODO add your handling code here:
        int applyoption = JOptionPane.showConfirmDialog(rootPane, "İşlemi Tamamlamak istediğinizden Emin Misiniz?");
        if (applyoption == JOptionPane.YES_OPTION) {
            if (NameField.getText().isEmpty() && SurnameField.getText().isEmpty() && AgeField.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Lütfen Bütün alanları doldurun");
            } else {
                EntityManagerFactory emf = Persistence.createEntityManagerFactory("JobApplicationTrackingSystemPU");
                EntityManager em = emf.createEntityManager();

                em.getTransaction().begin();

                Applicants applicant = new Applicants();

                applicant.setId(randomize());
                applicant.setName(NameField.getText());
                applicant.setSurname(SurnameField.getText());
                applicant.setAge(Integer.valueOf(AgeField.getText()));
                applicant.setGender(GenderComboBox.getSelectedItem().toString());
                applicant.setApplicationposition(PositionComboBox.getSelectedItem().toString());
                applicant.setApplicationtext(ApplicationTextfield.getText());
                applicant.setFlag(0);

                em.persist(applicant);
                em.getTransaction().commit();
                em.close();
                emf.close();

                this.dispose();

            }

        }

    }//GEN-LAST:event_RegisterButtonActionPerformed

