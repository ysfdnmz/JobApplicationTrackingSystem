    private int randomize() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("JobApplicationTrackingSystemPU");
        EntityManager em = emf.createEntityManager();

        Query q = em.createQuery("Select a from Applicants a ");
        Random r = new Random();

        int randomid = r.nextInt(10000);
        List<Applicants> applicantlist = q.getResultList();

        for (Applicants applicants : applicantlist) {
            if (applicants.getId() == randomid) {
                randomid = randomize();
            }

        }
        em.close();
        emf.close();

        return randomid;
    }

